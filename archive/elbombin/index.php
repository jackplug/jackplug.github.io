			<p class="introduction">The man labelled traitor who inspired a &#8220;Wunderteam&#8221;, the &#8220;Magic Magyars&#8221; and the modern game.</p>
			<p>Born October 16, 1882 in Nelson, Lancashire to Irish parents, Jimmy Hogan went on to become one of the most important and influential football coaches in Europe. As a skilled yet unrewarded inside forward, he played for Burnley, Bolton Wanderers, Rochdale, and Swindon Town, but it was at Fulham between 1905 and 1908 where the so-called &#8216;Scottish passing game&#8217; - inspired by coach Jock Hamilton - became a big influence upon his football philosophy, and this was the type of football he taught once his playing days were over.</p>
