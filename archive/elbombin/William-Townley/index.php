		<p>The outside-left William Townley, born in 1866, spent most of his playing career in the north-west of England. After starting out with Blackburn Olympic, he moved to their rivals Blackburn Rovers in the 1886-87 season. Whilst there, Townley not only won two full England caps (February 1889 and March 1890) but he famously became the first man ever to score a hat-trick in an FA Cup final, during the 6-1 victory over Sheffield Wednesday in 1890. He even scored in the following year&#8217;s final when Rovers retained the trophy with a 3-1 win over Notts County.</p>
		<p>Townley left Blackburn Rovers in July 1894 to join another local rival at the time: Darwen FC. After missing out on a place in the inaugural Football League formed in 1888, Darwen were a club in decline, but Townley stayed with them for two seasons before leaving to sign for Manchester City.</p>
		<p>After his playing days were over, Townley began what was to become an influential coaching career. As with many other English coaches unable to find work in their homeland, he taught what was known as the &#8216;Scottish passing game&#8217;, inspired by north-of-the-border players at Blackburn.</p>
		<p>He started coaching at DFC Prague (at that time playing in German competitions), but soon left to join FV Karlsruhe where he led them to the national trophy in 1909.</p>
		<p>Townley then moved on to SpVgg F&uuml;rth in April 1911 for the first of his three spells at the club, where he laid the foundations for their victory in the 1914 German National Championship by winning the Eastern region of the South German Championship. Unfortunately (for SpVgg at least) Bayern Munich decided that they wanted the English coach, and reluctantly, on 15 December 1913, SpVgg agreed to let him go.</p>
		<p>After World War I, facts about Townley are a little thin on the ground. As far as we can tell, he joined FSV Frankfurt during the 1916/17 season, then rejoined Bayern Munich in 1919 before moving to Switzerland to coach FC St. Gallen for a short spell in 1920.</p>
		<p>In January 1921, he took over at SV Waldhof Mannheim, but left in March of the same year. Two years later, he reappeared at FC St. Gallen, staying until 1925. Whilst at the Swiss club, he also coached the Netherlands national team between March and June 1924. His coaching career ended with two final spells at SpVgg F&uuml;rth, from May 1926 to September 1927, and September 1930 to June 1932.</p>
		<p>William Townley died in Blackpool in 1950 at the age of 84.</p>
