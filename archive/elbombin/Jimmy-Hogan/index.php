		<p>Born October 16, 1882 in Nelson, Lancashire to Irish parents, Jimmy Hogan went on to become one of the most important and influential football coaches in Europe. As a skilled yet unrewarded inside forward, he played for Burnley, Bolton Wanderers, Rochdale, and Swindon Town, but it was at Fulham between 1905 and 1908 where the so-called &#8216;Scottish passing game&#8217; - inspired by coach Jock Hamilton - became a big influence upon his football philosophy, and this was the type of football he taught once his playing days were over.</p>
		<p>A summer tour of the Netherlands with Bolton, where they easily beat Dordrecht made him vow to return and &#8220;teach them how to play.&#8221; In 1910, in his early 30s, he did exactly that, but it wasn&#8217;t long before the General Secretary of the Austrian Football Association (and later national team manager) Hugo Meisl took him to Austria to coach the national team. While working in Austria, he realised the value of maintaining a controlled diet - cutting down on meat and increasing fruit and vegetables. This was, at that time, a remarkable piece of thinking - even many years later English coaches generally thought that a good training session should be made up of a half hour&#8217;s run on the beach followed by a five-course meal. The Austrian team that Hogan and Meisl created went on to become known as the &#8216;Wunderteam&#8217; of 1931-32, taking famous victories against Scotland (5-0), Germany (6-0 and 5-0), Switzerland (8-1) and Hungary (8-2), and narrowly losing to England 4-3.</p>
		<p>At the outbreak of World War I, he was interned, but he was allowed to go to Hungary where he worked with the MTK Hungária club - the team that formed the basis of the &#8216;Magic Magyars&#8217; of the 1950s. This Hungarian national side, featuring Ferenc Puskas, Sandor Kocsis, and Nandor Hidegkuti, would go on to beat England twice (6-3 at Wembley in 1953, 7-1 the following spring in Budapest), and would be expected to win the 1954 World Cup.</p>
		<p>After the war, he returned to England, but was soon back in mainland Europe once more, coaching the future German World Cup-winning manager Helmut Sch&ouml;n at SC Dresden, and guiding the Austrians to the 1936 Olympic Final.</p>
		<p>Hogan did have some managerial and coaching spells in England. He took over at Fulham in August 1934, but the players disliked his &#8216;unconventional&#8217; training methods (he actually used a ball) and tactics, and there were many complaints about his style from the more established players. After 31 games, Hogan was sacked whilst recovering from an operation in hospital - the board stating that &#8220;seasoned professionals did not need coaching.&#8221;</p>
		<p>He had a little more success at Aston Villa, where he took over in 1936 until the outbreak of the World War II in 1939 - they were promoted from Division Two and managed to get to the semi-final of the FA Cup. During the 1950s, Hogan returned to coach the Villa youth team, and it is said that in 1953 he took a group of apprentices on a day out to Wembley to watch &#8220;something memorable&#8221; - a little-respected Hungarian side that dedicated their victory to a man largely unknown in his homeland.</p>
		<p>Jimmy Hogan spent the final years of his life near Turf Moor, the home of his beloved Burnley. When Burnley won the 1971-72 Second Division Championship, he was invited to a match against Watford as Guest of Honour - one of the disgracefully few honours given to him. When he died in Burnley General Hospital in 1974, aged 91, tributes came in from all over Europe, with the German FA hailing him as one of the founders of the modern game, responsible for their later successes.</p>
		<p>Despite his accomplishments on the Continent, Hogan was patriotic and would emphasise his British-ness.</p>
		<p>&#8220;I am a British coach,&#8221; he would say, &#8220;I still maintain that we have the best players, but it is our style of playing the game that has gone wrong.&#8221;</p>
		<p>He continually warned the FA that English football would be overtaken by the &#8216;Continentals&#8217;, but to no avail. In a country where it was believed that keeping a ball away from the players during the week would increase their eagerness to get it on match-day, his love of the short pass, exploitation of space, good technique, and hatred of the mindless long ball would always prove difficult to get across.</p>
