			<h3><a href="/William-Townley/" title="more about William Townley">William Townley: From Blackburn To Bavaria</a></h3>
			<p class="introduction">The F&uuml;rth coach who was the first man to score a hat-trick in an FA Cup final.</p>
			<p>The outside-left William Townley, born in 1866, spent most of his playing career in the north-west of England. After starting out with Blackburn Olympic, he moved to their rivals Blackburn Rovers in the 1886-87 season. Whilst there, Townley not only won two full England caps (February 1889 and March 1890) but he famously became the first man ever to score a hat-trick in an FA Cup final, during the 6-1 victory over Sheffield Wednesday in 1890. He even scored in the following year's final when Rovers retained the trophy with a 3-1 win over Notts County.</p>
