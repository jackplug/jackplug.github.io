		<p>Jack Reynolds (actually, &#8216;John&#8217; Reynolds) began a rather unexceptional playing career at the age of 21 in Manchester City's reserve team before spending a season at Grimsby Town, making his debut in September 1904. After the side finished 13th in the old Second Division, he was on his way again, and in the remainder of his 12 year career, he played for Sheffield Wednesday, Watford, and Gillingham (known at that time as New Brompton.)</p>
		<p>After coaching the Swiss side St. Gallen from 1912 to 1914, Reynolds began the coaching career that made his name with Ajax of Amsterdam. In 1914, when he was due to take over as German national team coach, the outbreak of World War I forced him to move to the Netherlands, and in 1915 he began the first spell of his 25 seasons associated with Ajax.</p>
		<p>Reynolds brought new ideas to the team, ideas that would later be considered the &#8216;Ajax-style&#8217;: intelligent, skillful,  quick-passing attacking football played with wingers. It was Reynolds that introduced the cross-field pass that would &#8216;stretch&#8217; the shape of the opposition sideways, providing more space for the attacking side. In an interview in 1946, Reynolds emphasised this: &#8220;For me, the attack is and remains the best defence.&#8221;</p>
		<p>His strict discipline, and training that emphasised technique and passing as well as fitness, were revolutionary in the early years of the 20th century, but he worked hard at his job too, according to an unpublished biography of Reynolds by historian Harke Groenevelt, &#8220;working from eight every morning until ten at night coaching teams of every age group in the same style&#8221;, laying the foundations for the highly successful Ajax youth system.</p>
		<p>Reynolds&#8217;s time with Ajax was interrupted by two events. In 1925, after an argument with the board, he left to join local rivals Blauw-Wit, but he only stayed there for 3 years before returning.</p>
		<p>The second occasion was a little more disturbing. When the Nazis invaded Amsterdam during the Second World War, Reynolds, as a British citizen, was interned at the Tost detention camp in the city of Gleiwitz in Upper Silesia at the same time as the author P.G. Wodehouse. Ajax attempted to stay in contact with Reynolds during this time, sending him letters and parcels, but due to the understandable confusion at the end of the war, thought him dead. It turned out that he had survived and had been repatriated back to the UK, and in 1945 he resumed his job with the club, staying until 1947.</p>
		<p>Reynolds remained in Amsterdam until his death in 1962, and in 1965, Ajax named the stand opposite the main stand at the De Meer stadium after him - to commemorate the man who was their longest-serving coach.</p>
