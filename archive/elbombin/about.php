		<h2 id="whatIsEB">What Is El Bomb&iacute;n?</h2>
		<p><strong>El Bomb&iacute;n</strong> is a collection of articles and information highlighting the achievements of British football coaches working <strong>OUTSIDE</strong> Great Britain, from the early days of football until the 1960s. Many of these coaches were generally overlooked in their homeland due to their &#8216;radical&#8217; ideas on how the game should be played - passing, ball possession and even training sessions with a ball were looked at unfavourably by some of the early football establishment.</p>

		<p>There are three aims to this web site. Firstly, to document the work of those coaches who believed that the twin objectives of a successful team and playing so-called &#8216;beautiful football&#8217; need not be mutally exclusive. The second aim is to provide an easy-to-find, easy-to-use, accessible resource to those looking for information regarding these forgotten coaches; and the third aim is to hopefully pay tribute to their work.</p>

		<h2 id="aboutTheName">About The Name</h2>
		<p>The name <strong>El Bomb&iacute;n</strong> came from a book called &#8216;<a href="http://www.amazon.co.uk/exec/obidos/ASIN/0954013468/stuarthomfray-21">Morbo: The Story Of Spanish Football</a>&#8217;, written by Phil Ball. &#8216;Bomb&iacute;n&#8217; was the nickname of a manager of Athletic Bilbao in the 1920s and early 1930s named <a href="/Fred-Pentland/">Fred Pentland</a>, and it means &#8216;bowler hat&#8217; - how can you get more British than that??</p>
