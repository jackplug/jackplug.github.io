		<h2 id="theInfoOnTheSite">About the information on El Bomb&iacute;n</h2>
		<p>Providing accurate information <strong>WHEREVER POSSIBLE</strong> is integral to this site - in fact, there&#8217;s no point setting it up otherwise! In order to do this, <strong>El Bomb&iacute;n</strong> aims to have <strong>AT LEAST TWO SEPARATE</strong> references for any facts presented here (ideally, even more!) This does not, of course, guarantee with absolute certainty that something is true, but, with the additions and corrections by visitors to the site, the intention is to create and maintain highly accurate records.</p>

